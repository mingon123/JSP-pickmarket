package kr.location.vo;

public class LocationVO {
	private String region_cd;
	private String sido_cd;
	private String sgg_cd;
	private String umd_cd;
	private String region_nm;
	
	public String getRegion_cd() {
		return region_cd;
	}
	public void setRegion_cd(String region_cd) {
		this.region_cd = region_cd;
	}
	public String getSido_cd() {
		return sido_cd;
	}
	public void setSido_cd(String sido_cd) {
		this.sido_cd = sido_cd;
	}
	public String getSgg_cd() {
		return sgg_cd;
	}
	public void setSgg_cd(String sgg_cd) {
		this.sgg_cd = sgg_cd;
	}
	public String getUmd_cd() {
		return umd_cd;
	}
	public void setUmd_cd(String umd_cd) {
		this.umd_cd = umd_cd;
	}
	public String getRegion_nm() {
		return region_nm;
	}
	public void setRegion_nm(String region_nm) {
		this.region_nm = region_nm;
	}
	
}
